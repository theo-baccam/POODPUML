LevelBuilder::LevelBuilder() {}
LevelBuilder::~LevelBuilder() {}

void LevelBuilder::buildEasyLevel(flecs::world &world) {
    const unsigned int mapDimensions = 16;

    for (int y = 0; y < mapDimensions; y++) {
        for (int x = 0; x < mapDimensions; x++) {
            if ((x == 0 && y == 0) || (x == 15 && y == 15)) continue;
            world.entity()
                .add<FloorTag>()
                .set<Position>({(double) x, (double) y});
        };
    };

    world.entity("Start")
        .add<FloorTag>()
        .set<Position>({0, 0});
    world.entity("End")
        .add<FloorTag>()
        .set<Position>({15, 15});
}

void LevelBuilder::buildMediumLevel(flecs::world &world) {
    const char map[] =
        ".........##S####........\n"
        ".......###########......\n"
        "...################.....\n"
        "..##################....\n"
        ".####################...\n"
        ".#########...########...\n"
        ".#######......########..\n"
        "########......#########.\n"
        "############..####..####\n"
        "##################..####\n"
        "######....#########..##.\n"
        "######......#######..##.\n"
        "######################..\n"
        "#########.###########...\n"
        ".######...##########....\n"
        ".######..########.......\n"
        "..#############.........\n"
        "..#############.........\n"
        ".......#########........\n"
        ".....#############......\n"
        "..################......\n"
        "..##################....\n"
        "...#################....\n"
        "....#E##....######......";

    int x = 0;
    int y = 0;
    for (char c : map) {
        switch (c) {
            case '#':
                world.entity()
                    .add<FloorTag>()
                    .set<Position>({(double) x, (double) y});
                break;
            case 'S':
                world.entity("Start")
                    .add<FloorTag>()
                    .set<Position>({(double) x, (double) y});
                break;
            case 'E':
                world.entity("End")
                    .add<FloorTag>()
                    .set<Position>({(double) x, (double) y});
                break;
            case '\n':
                x = 0;
                y++;
                break;
        };
        x++;
    };
}

